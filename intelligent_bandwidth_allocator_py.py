# Generate some random input data
batch_size = 10
sequence_length = 20
x = torch.randint(0, 256, (batch_size, sequence_length))

# Instantiate the model
model = IntelligentBandwidthAllocator()

# Forward pass
output_probs = model(x)  # shape: [batch_size, num_classes]

# Print output probabilities
print("Predicted probabilities:\n", output_probs)

